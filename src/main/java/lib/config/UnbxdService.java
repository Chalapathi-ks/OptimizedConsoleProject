package lib.config;

public class UnbxdService {



    private String name;
    private String url;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }



    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getHost() {
        return url;
    }

    public void setHost(String host) {
        this.url = host;
    }

}
